// String = texto (uso de aspas)

console.log('Luiz Otávio'); // Pode ser utilizada com aspas duplas ("''")
console.log("Luiz Otávio"); // Pode ser utilizada coma aspas simples ('"')
console.log(`Luiz Otávio`); // Pode ser utilizada com as aspas duplas e simples (`'Luiz' "Otávio"`)

// Number

console.log(123456, 15.85, 'Luiz Otávio de novo'); // valor literal - números reais (números de pontos flutuantes). Uso de pontos e vírgulas para separação dos números. 
 